package com.scramble_like.game.utils;

public class RectangleData
{
    public int x, y, width, height, i, j;

    public RectangleData(int x, int y, int width, int height, int i, int j)
    {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.i = i;
        this.j = j;
    }
}
